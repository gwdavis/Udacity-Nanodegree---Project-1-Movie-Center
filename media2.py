import webbrowser


    
class Movie():
   """This class provide a way to store and show video information"""

def __init__(self):
        self.title = ""
        self.storyline = ""
        self.poster_image_url = ""
        self.genre = ""
        self.duration = ""
        self.rating = ""
        self.release_date = ""
        self.trailer_youtube_id = ""

def show_trailer(self):
        webbrowser.open(self.trailer_youtube_url)

        
    
        
